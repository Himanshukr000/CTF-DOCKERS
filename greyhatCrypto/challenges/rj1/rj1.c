#include "rj1.h"

static const uint8_t sbox[256];
static const uint8_t sinv[256];

void rj1_enc(uint8_t *block, const uint8_t *key) {

	for (int round = 0; round < 8; round++) {

		// mix keys and substitute
		for (int i = 0; i < 8; i++) {
			block[i] = sbox[block[i] ^ key[(i + round) & 7]];
		}

		// diffuse
		for (int i = 0; i < 8; i++) {
			if (i == round) continue;
			block[i] ^= block[round];
		}
	}
}

void rj1_dec(uint8_t *block, const uint8_t *key) {

	for (int round = 7; round >= 0; round--) {

		// reverse diffuse
		for (int i = 0; i < 8; i++) {
			if (i == round) continue;
			block[i] ^= block[round];
		}

		// reverse mix keys and substitute
		for (int i = 0; i < 8; i++) {
			block[i] = sinv[block[i]] ^ key[(i + round) & 7];
		}
	}
}

